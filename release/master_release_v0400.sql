         http://stackoverflow.com/questions/31431197/select2-how-to-prevent-tags-sorting
      s2item.on("select2:select", function(e) {
        var $element = $(e.params.data.element);

        $element.detach();
        $(this).append($element);
        $(this).trigger("change");
      });';
